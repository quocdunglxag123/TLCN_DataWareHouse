package dto;


public class FactTradeChart {
	private String companyName;
	private String total_volume;
	private String total_price;
	private String total_marketcapitalization;

	public FactTradeChart() {
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getTotal_volume() {
		return total_volume;
	}

	public void setTotal_volume(String total_volume) {
		this.total_volume = total_volume;
	}

	public String getTotal_price() {
		return total_price;
	}

	public void setTotal_price(String total_price) {
		this.total_price = total_price;
	}

	public String getTotal_marketcapitalization() {
		return total_marketcapitalization;
	}

	public void setTotal_marketcapitalization(String total_marketcapitalization) {
		this.total_marketcapitalization = total_marketcapitalization;
	}
	

}
